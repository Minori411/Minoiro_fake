function clickAction(name, code)
{
	cursel = 0;
	//##########################################
	// 現在の選択対象（何番目のボトルか）を取得
	//##########################################
	for( i=0;i < 4;i++)
	{
		if(bottlesArray[i].code == -1)
		{
			cursel = i+1;
			break;
		}
	}
//alert(cursel);
	if( 1 <= cursel && cursel <= 4 )
	{
		curimg=$("#bottle" + cursel);
		re = /\d+/i;
		pos = code.search(re);
		//##########################################
		// 既に選ばれたボトルならエラー表示
		//##########################################
		for( i=0;i < 4;i++)
		{
			if(bottlesArray[i].code == parseInt(code.substr(pos),10))
			{
//				alert(msg4);
				return;
			}
		}
		//##########################################
		// 現在の選択対象ボトルにクリックされたボトル画像を表示
		//##########################################
		curimg.attr("class","");
		curimg.css("background","url("+bottleImgM2 + "B" + PadtoString( parseInt(code.substr(pos),10), 3,"0") + ".jpg) no-repeat");
		curimg.css("padding-top", 12);
		bottlesArray[cursel-1] = new bottle( name, parseInt(code.substr(pos),10));
		//##########################################
		// 現在の選択対象ボトルに対する「選びなおす」ボタンを表示
		//##########################################
		$("#del" + cursel).css("display", "block");
		$("#enlarge"+cursel).css("display", "block");
		//##########################################
		// 選ばれたボトル位置に空ボトルを表示する
		//##########################################
		selbottle=$("#"+code);
		selbottle.attr("src", selectedImg);
		//##########################################
		// 次の選択ボトル表示位置を表示する
		//##########################################
		for( i=0;i < 4;i++)
		{
			if(bottlesArray[i].code == -1)
			{
				nextsel = i+1;
				nextimg=$("#bottle" + nextsel);
//				nextimg.css("background","");
				nextimg.attr("class", emptyImg2 + nextsel);
				break;
			}
		}
		//##########################################
		// 4本が選ばれたら結果表示ボタンを表示する
		//##########################################
		if( i == 4 )
		{
//			$("#bringup").css({height:0,"margin-bottom":0,"opacity":0});
			$("#bringup").animate({height:50,"margin-bottom":25,"opacity":1},500);
//			$("#reading_title").css("background-position", "0 -72px");
		}
	}
	else
	{
		alert("既に４本のボトルが選ばれています！");
	}
	mouseOutAction();
	return;
}
